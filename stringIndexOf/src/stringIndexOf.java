
public class stringIndexOf {

	public static void main(String[] args) {
		String string="abcdefghijklmnopqrstuvwxyzabc";
		System.out.println(string.indexOf('a'));
		System.out.println(string.lastIndexOf('a'));
		System.out.println(string.indexOf('b'));
		System.out.println(string.lastIndexOf('b'));
		System.out.println(string.indexOf("abc"));
		System.out.println(string.lastIndexOf("abc"));
		
		
	}

}
